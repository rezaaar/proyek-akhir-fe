<script>
import axios from 'axios'
import { initModals } from 'flowbite'

definePageMeta({
    layout: "admin",
})

export default {
    data() {
        return {
            articles: [],
            selectedArticle: {},
            form: {
                article: {
                    title: '',
                }
            }
        }
    },
    async mounted() {
        await this.getArticle()
        initModals()
    },
    methods: {
        async getArticle() {
            await axios.get(useRuntimeConfig().public.API_URL + 'article').then(
                (res) => {
                    this.articles = res.data
                }
            )
        },

        async getOneArticle(id) {
            await axios.get(useRuntimeConfig().public.API_URL + 'article/' + id).then(
                (res) => {
                    this.selectedArticle = res.data
                }
            )
        },

        async updateArticle(data) {
            await axios.put(useRuntimeConfig().public.API_URL + 'article/' + this.selectedArticle._id, {
                title: this.selectedArticle.title,
                description: this.selectedArticle.description,
                category: this.selectedArticle.category,
                publisher: "reza"
            }).then(
                (res) => {
                    this.getArticle()
                }
            )
        },
        async postArticle(data) {
            await axios.post(useRuntimeConfig().public.API_URL + 'article', {
                title: this.selectedArticle.title,
                description: this.selectedArticle.description,
                category: this.selectedArticle.category,
                publisher: "reza"
            }).then(
                (res) => {
                    this.getArticle()
                }
            )
        },
    }
}
</script>