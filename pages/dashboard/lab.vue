<script>
import axios from 'axios'
import { initModals } from 'flowbite'

definePageMeta({
    layout: "admin",
})

export default {
    data() {
        return {
            roles: [],
            dosens: [],
            dataLab: [],
            form: {
                lab: {
                    name: "",
                    leader: "",
                    description: "",
                }
            }
        }
    },

    async mounted() {
        initModals()
        await this.getDataLab()
        await this.getListDosen()
    },

    methods: {
        async getDataLab() {
            await axios.get(useRuntimeConfig().public.API_URL + "lab")
                .then((response) => {
                    this.dataLab = response.data
                })
                .catch((error) => {
                    console.log(error)
                })
        },

        handleEdit(id) {
            this.$router.push({
                path: '/dashboard/admin/user/edit',
                query: { id: id }
            })
        },

        setFormLabNull() {
            this.form.lab.name = ''
            this.form.lab.leader = ''
            this.form.lab.description = ''
        },

        async createLab() {
            await axios.post(useRuntimeConfig().public.API_URL + 'lab', this.form.lab).then(
                async (res) => {
                    await this.getDataLab()
                    this.setFormLabNull()
                    console.log("Berhasil");
                }
            ).catch((err) => console.log(err))
        },

        async getListDosen() {
            await axios.get(useRuntimeConfig().public.API_URL + 'dosen').then((res) => this.dosens = res.data)
                .catch((err) => console.log(err))
        }
    }

}
</script>